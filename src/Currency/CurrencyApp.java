    public int linearSearch(String query) {
        for(int i = 0; i < currencyType.length; i++) {
            if (currencyType[i] == query) {
                return i;
            }
        }
        return -1;
    }
    
    public static Currency[] createObjectArray(double amt) {
        Currency[] objectList = {
            new USD(amt),
            new EURO(amt),
            new GBP(amt),
            new JPY(amt),
            new KRW(amt),
            new AUD(amt),
            new CAD(amt),
            new CNY(amt),
            new MXN(amt)
            
        };
        return objectList;
    } 

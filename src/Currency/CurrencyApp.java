    private void convertCurrencyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertCurrencyButtonActionPerformed
        String currentSelection = (String) currentCurrency.getSelectedItem();
        String convertSelection = (String) convertCurrency.getSelectedItem();

        //try except
        double userCurrencyInput = 0.00;

        try {
            userCurrencyInput = Double.parseDouble(currentCurrencyInput.getText().toString());
            int currentSelectionIndex = linearSearch(currentSelection);  //Obtain index of chosen user quantity
            
             //Create array of the Currency Objects in respective Order as they are listed in currencyType[]
            Currency currencyObjectArray[] = createObjectArray(userCurrencyInput); //Since the objectArray is sorted to match the JComboBox array, it will take the integer index value from currentSelectionIndex and use that to select the correct Currency Object 
           
            //Performs the convert method of the Currency Class. This is then printed out in the toString method in the displayPanelLabel
            currencyObjectArray[currentSelectionIndex].convert(convertSelection);

            displayPanelLabel.setText(currencyObjectArray[currentSelectionIndex].toString() + " " + convertSelection);
        } 
        catch (Exception e) {
            System.out.println("Error: Invalid Input");
            displayPanelLabel.setText("Please enter only digits, 0-9, no special characters or spaces!");
        }
        
    }//GEN-LAST:event_convertCurrencyButtonActionPerformed

